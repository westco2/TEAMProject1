package com.upload.model;

public class upVO {
	
	public String path;
	public String file;
	public upVO(String path, String file) {
		super();
		this.path = path;
		this.file = file;
	}
	public upVO() {
		super();
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public String getFile() {
		return file;
	}
	public void setFile(String file) {
		this.file = file;
	}

	
	
	

	

	
	
}
